def cuadrados(p1,p2,p3):
    pa1=p1**2
    pa2=p2**2
    pa3=p3**2
    print("El cuadrado del primer numero es "+str(pa1))
    print("El cuadrado del segundo numero es "+str(pa2))
    print("El cuadrado del tercer numero es "+str(pa3))

num1=int(input("Ingrese el primero numero, por favor: \n>"))
num2=int(input("Ingrese el segundo numero, por favor: \n>"))
num3=int(input("Ingrese el tercer numero, por favor: \n>"))
cuadrados(num1,num2,num3)
